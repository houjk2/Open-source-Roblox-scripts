	local Players = game:GetService("Players")
	local Frame = script.Parent.Frame
	local Box = script.Parent.Deez.UserName
	
	game:GetService("UserInputService").InputBegan:Connect(function(inpt)
		if inpt.KeyCode == Enum.KeyCode.RightShift then
			if script.Parent.Enabled == true then
				script.Parent.Enabled = false
			else 
				script.Parent.Enabled = true
			end
		end
	end)
	
	function startswith(startswith, thestring)
		if string.sub(thestring, 1, string.len(startswith)) == startswith then
			return true
		else
			return false
		end
	end
	
	function Refresh()
		for i3, v3 in pairs(Frame:GetChildren()) do 
			v3.Text = "Label"
		end
		
		
		if Box.Text == "" then
			for i, v in pairs(Players:GetPlayers()) do 
				local lvl = v.PlayerValues.Level.Value
				for i2, v2 in pairs(Frame:GetChildren()) do 
					if v2.Text == "Label" then
						v2.Text = v.Name.." - "..lvl
						break
					end
				end
			end
		else 
			for i, v in pairs(Players:GetPlayers()) do 
				local lvl = v.PlayerValues.Level.Value
				if startswith(Box.Text, v.Name) == true then
					for i2, v2 in pairs(Frame:GetChildren()) do 
						if v2.Text == "Label" then
							v2.Text = v.Name.." - "..lvl
							break
						end
					end
				end
			end
		end
	end
	
	
	
	Players.PlayerRemoving:Connect(function(plr)
		for i, v in pairs(Frame:GetChildren()) do 
			if string.sub(v.Text, 1, string.len(v.Text) - 3) == plr.Name then
				v.Text = "Label"
			end
		end
	end)
	
	while true do 
		wait(0.1)
		Refresh()
	end
	
end
